const DIR = new URL('../src/data/', import.meta.url).pathname;
const files = readdirSync(DIR)
  .filter((f) => /^_vocab_extended_part\d+\.js$/.test(f))
  .sort();

let totalFixed = 0;
for (const f of files) {
  const path = join(DIR, f);
  const src = readFileSync(path, 'utf8');
  let changed = 0;
  const out = src.replace(/root: '([^']+)'/g, (m, root) => {
    if (CORRECTIONS[root] && CORRECTIONS[root] !== root) {
      changed++;
      return `root: '${CORRECTIONS[root]}'`;
    }
    return m;
  });
  if (changed > 0) {
    writeFileSync(path, out);
    console.log(`${f}: ${changed} root corrections applied`);
    totalFixed += changed;
  }
}
console.log(`\nTotal: ${totalFixed} root corrections`);
